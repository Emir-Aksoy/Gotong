#!/usr/bin/env node
// Generate src/static-assets.ts from static/*.
//
// Why: `bun build --compile` produces a single-file binary that has no
// filesystem the way a normal `node` install does — `fs.readFile` from a
// path computed off `import.meta.url` cannot find sibling files. So for
// the binary distribution path we embed every static asset as a base64
// string at build time. The same generated module is harmless in npm /
// docker installs (it's just an extra ~300 KB of constant data that gets
// shipped alongside the on-disk static/ directory, which we keep for
// people who want to swap an asset without a rebuild).
//
// Re-run: `pnpm -C packages/web build:assets`. The `build` script invokes
// this automatically before `tsc`, so a normal `pnpm -r build` keeps the
// committed src/static-assets.ts in sync with static/. CI runs the same
// build, so a stale checkout will surface as a clean-tree diff failure.

import { readdir, readFile, stat, writeFile } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const PKG_ROOT = join(__dirname, '..')
const STATIC_DIR = join(PKG_ROOT, 'static')
const OUT_FILE = join(PKG_ROOT, 'src', 'static-assets.ts')

async function listFiles(dir) {
  const out = []
  for (const entry of await readdir(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name)
    if (entry.isDirectory()) {
      const nested = await listFiles(full)
      for (const n of nested) out.push(n)
    } else if (entry.isFile()) {
      out.push(full)
    }
  }
  return out
}

const files = await listFiles(STATIC_DIR)
files.sort()

const entries = []
let totalBytes = 0
for (const full of files) {
  const rel = full.slice(STATIC_DIR.length + 1).replaceAll('\\', '/')
  const buf = await readFile(full)
  totalBytes += buf.length
  // base64 keeps the source file diff-friendly (one chunk per asset, no
  // hex-array noise) and decodes via the global atob in every supported
  // runtime (Node 16+, Bun, browsers).
  const b64 = buf.toString('base64')
  entries.push(`  ${JSON.stringify(rel)}: ${JSON.stringify(b64)},`)
}

const header = `// AUTO-GENERATED by scripts/build-static-assets.mjs. Do not edit by hand.
// Run \`pnpm -C packages/web build:assets\` after changing static/*.
//
// Embeds every file under static/ as a base64 string so the production
// build (especially bun --compile single-file binaries) can serve them
// without filesystem access. Total embedded size: ${totalBytes} bytes
// (${files.length} files). server.ts decodes lazily and caches.
`

const body = `\nexport const STATIC_ASSETS_BASE64: Record<string, string> = {\n${entries.join('\n')}\n}\n`

await writeFile(OUT_FILE, header + body, 'utf8')

// Echo a one-liner so `pnpm build` output shows what happened without
// being noisy. Total file count + bytes is enough to spot a regression
// where the generator silently emitted nothing.
process.stdout.write(`build-static-assets: ${files.length} files, ${totalBytes} bytes embedded → ${OUT_FILE}\n`)
