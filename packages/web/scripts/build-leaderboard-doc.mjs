#!/usr/bin/env node
// Render the citation leaderboard into a CHECKED-IN, marked region of
// docs/zh/FLAGSHIP-TEMPLATES.md — so the ranking is visible in the repo itself,
// without anyone first having to build + deploy the static site.
//
// Why a generator (not a hand-kept table): the leaderboard is "how many
// templates declare provenance.derivedFrom you" — an in-degree over the real
// corpus. A hand-maintained table goes stale the moment someone adds an edge.
// This reads the SAME validated corpus and the SAME in-degree computation as the
// storefront (build-site.mjs `loadCorpus` + `buildModel`), so the doc table and
// the site leaderboard can never disagree.
//
// Drift guard: tests/build-leaderboard-doc.test.ts re-runs this render against
// the real corpus and asserts the checked-in region matches byte-for-byte — so
// adding a derivedFrom edge without regenerating fails CI, named, instead of
// rotting silently.
//
// DETERMINISTIC: no timestamp, stable sort (inherited from buildModel). Same
// corpus → byte-identical block → no doc churn on rebuild.
//
// Re-run: `pnpm build:leaderboard` (root) or `pnpm -C packages/web build:leaderboard`.

import { readFile, writeFile } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'
import { loadCorpus, buildModel } from './build-site.mjs'

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = join(__dirname, '..', '..', '..')
const DOC_PATH = join(REPO_ROOT, 'docs', 'zh', 'FLAGSHIP-TEMPLATES.md')

// The markers fence the generated region. Editing between them by hand is
// pointless (the next `pnpm build:leaderboard` overwrites it) AND caught by the
// drift test — both say so right in the marker text.
export const MARK_START =
  '<!-- LEADERBOARD:START — generated by `pnpm build:leaderboard`; do not edit by hand -->'
export const MARK_END = '<!-- LEADERBOARD:END -->'

/** Reverse the derivedFrom edges: target slug -> sorted names of the templates
 * that cite it (the "谁基于它改的" column). Unresolved edges (a typo'd citation
 * that points at no real slug) are counted nowhere — same as buildModel. */
export function citedByMap(templates) {
  const bySlug = new Map(templates.map((t) => [t.slug, t]))
  const map = new Map()
  for (const t of templates) {
    for (const target of t.derivedFrom) {
      if (!bySlug.has(target)) continue
      if (!map.has(target)) map.set(target, [])
      map.get(target).push(t.name)
    }
  }
  for (const names of map.values()) {
    names.sort((a, b) => (a < b ? -1 : a > b ? 1 : 0))
  }
  return map
}

/** Pure render of the markdown block that lives between the markers. Takes the
 * buildModel result + the reverse-edge map; returns a deterministic string. */
export function renderLeaderboardMarkdown(model, citedBy) {
  if (model.leaderboard.length === 0) {
    return [
      MARK_START,
      '',
      '_还没有派生模板 —— 第一个声明 `provenance.derivedFrom` 的模板会出现在这里。_',
      '',
      MARK_END,
    ].join('\n')
  }
  const rows = model.leaderboard.map((e, i) => {
    const who = (citedBy.get(e.slug) ?? []).join('、') || '—'
    return `| ${i + 1} | **${e.name}** (\`${e.slug}\`) | ${e.citationCount} | ${who} |`
  })
  return [
    MARK_START,
    '',
    '| # | 模板 | 被引用次数 | 谁基于它改的 |',
    '|---|---|---|---|',
    ...rows,
    '',
    MARK_END,
  ].join('\n')
}

/** Replace the marked region in the doc text with `block`. Throws if the markers
 * are missing or out of order — a silent no-op would let the table rot. */
export function spliceRegion(docText, block) {
  const start = docText.indexOf(MARK_START)
  const end = docText.indexOf(MARK_END)
  if (start === -1 || end === -1 || end < start) {
    throw new Error(
      `FLAGSHIP-TEMPLATES.md is missing the leaderboard markers ` +
        `("${MARK_START}" … "${MARK_END}"). Add them where the table should ` +
        `render, then re-run pnpm build:leaderboard.`,
    )
  }
  return docText.slice(0, start) + block + docText.slice(end + MARK_END.length)
}

async function main() {
  const templates = await loadCorpus()
  const model = buildModel(templates)
  const block = renderLeaderboardMarkdown(model, citedByMap(templates))

  for (const u of model.unresolved) {
    process.stderr.write(
      `build-leaderboard-doc: WARNING ${u.from} derivedFrom '${u.to}' — no template with that slug (typo'd citation?)\n`,
    )
  }

  const doc = await readFile(DOC_PATH, 'utf8')
  const next = spliceRegion(doc, block)
  if (next !== doc) {
    await writeFile(DOC_PATH, next, 'utf8')
    process.stdout.write(
      `build-leaderboard-doc: updated FLAGSHIP-TEMPLATES.md (${model.leaderboard.length} template(s) ranked)\n`,
    )
  } else {
    process.stdout.write('build-leaderboard-doc: FLAGSHIP-TEMPLATES.md already current\n')
  }
}

// Only sweep the filesystem / write when executed directly — importing this
// module (the drift test) must not write the doc.
if (process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1]) {
  main().catch((err) => {
    process.stderr.write(`build-leaderboard-doc: ${err instanceof Error ? err.stack : String(err)}\n`)
    process.exit(1)
  })
}
