#!/usr/bin/env node
// Bundle admin-src/* into static/admin.js via esbuild.
//
// Why: the admin console grew past 3300 lines in a single hand-written
// static/admin.js. P3 (admin UI split) moves the source under admin-src/
// as ES modules and lets esbuild stitch them back into one IIFE file so
// the runtime story is unchanged — app.js still injects a single
// <script src="/admin.js">, window.Gotong coordination still works, and
// the single-file binary still embeds one admin.js via static-assets.ts.
//
// IIFE (not ESM) output: admin.js loads as a classic <script>, not
// type=module, and must run inside the existing app-core → admin-wf-assist
// → admin.js load order. IIFE keeps that contract byte-for-byte.
//
// charset:utf8 keeps the i18n strings (Chinese copy, … glyphs) as real
// UTF-8 instead of \uXXXX escapes — smaller output and a readable diff.
//
// Re-run: `pnpm -C packages/web build:admin`. The `build` / `build:assets`
// chain runs this before build-static-assets.mjs so the embedded asset
// stays in sync with the bundle.

import { build } from 'esbuild'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const PKG_ROOT = join(__dirname, '..')

const banner = `// AUTO-GENERATED by scripts/build-admin-ui.mjs from admin-src/. Do not edit by hand.
// Edit admin-src/*.js then run \`pnpm -C packages/web build:admin\`.`

const result = await build({
  entryPoints: [join(PKG_ROOT, 'admin-src', 'main.js')],
  bundle: true,
  format: 'iife',
  target: ['es2020'],
  charset: 'utf8',
  // Not minified: the embedded copy ships in the binary either way, and an
  // unminified bundle keeps the static-assets.ts diff legible when a
  // section changes. Flip to true if binary size ever matters more.
  minify: false,
  banner: { js: banner },
  outfile: join(PKG_ROOT, 'static', 'admin.js'),
  logLevel: 'warning',
})

if (result.warnings.length > 0) {
  process.stdout.write(`build-admin-ui: ${result.warnings.length} warning(s)\n`)
}
process.stdout.write('build-admin-ui: admin-src/main.js → static/admin.js (iife)\n')
