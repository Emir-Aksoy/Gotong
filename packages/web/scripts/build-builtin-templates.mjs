#!/usr/bin/env node
// Generate src/builtin-templates.ts from a CURATED list of shipped
// `gotong.template/v1` files under examples/*/template/.
//
// Why (Track G — 模板画廊): the template gallery lets an admin browse the
// hub templates we ship and install one with a click. The install path
// already exists (POST /api/admin/templates/import). The gap was packaging:
// the gallery needs the template manifests available to the web server at
// runtime. Rather than read them off disk (they live in examples/, which a
// production binary may not ship), we embed the curated set as a constant —
// same trick as build-static-assets.mjs.
//
// SINGLE SOURCE OF TRUTH stays in examples/ (模版与框架分离): this file is a
// derived copy. The web anti-corruption test (builtin-templates.test.ts)
// re-parses every embedded manifest through the REAL parseTemplate, so a
// drift between the committed copy and a changed example surfaces as a test
// failure; CI runs the build, so a stale checkout surfaces as a clean-tree
// diff.
//
// The list is CURATED (an explicit allow-list, not a glob): adding a new
// example template does not auto-publish it to the gallery — you opt it in
// here with a stable `id`. The `id` is decoupled from the filename so a file
// rename can't silently change a gallery entry's identity.
//
// Re-run: `pnpm -C packages/web build:templates`. The `build` script invokes
// this automatically before `tsc`.

import { readFile, writeFile } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const PKG_ROOT = join(__dirname, '..')
const REPO_ROOT = join(PKG_ROOT, '..', '..')
const OUT_FILE = join(PKG_ROOT, 'src', 'builtin-templates.ts')

// The curated gallery, in display order: personal hubs, then organization
// (management / collaboration), then cross-organization, then the family
// learning pair. `id` is the stable gallery id (NOT the filename); `file` is
// relative to the repo root.
const CURATED = [
  { id: 'personal-coding-hub', file: 'examples/personal-coding-hub/template/personal-coding-hub.template.yaml' },
  { id: 'codex-deepseek-hub', file: 'examples/codex-deepseek-hub/template/codex-deepseek-hub.template.yaml' },
  { id: 'personal-research-hub', file: 'examples/personal-research-hub/template/personal-research-hub.template.yaml' },
  { id: 'morning-brief-hub', file: 'examples/morning-brief-hub/template/morning-brief-hub.template.yaml' },
  { id: 'solo-company-hub', file: 'examples/solo-company-hub/template/solo-company-hub.template.yaml' },
  { id: 'battle-monk-training', file: 'examples/battle-monk-training/template/battle-monk-training.template.yaml' },
  { id: 'smart-home-hub', file: 'examples/smart-home-hub/template/smart-home-hub.template.yaml' },
  { id: 'family-hub', file: 'examples/family-hub/template/family-hub.template.yaml' },
  { id: 'cafe-ops', file: 'examples/cafe-ops/template/cafe-ops.template.yaml' },
  { id: 'warband-club', file: 'examples/warband-club/template/warband-club.template.yaml' },
  { id: 'tea-supply-link', file: 'examples/tea-supply-link/template/tea-shop.template.yaml' },
  { id: 'tea-chain-hq', file: 'examples/tea-chain-hq/template/chain-hq.template.yaml' },
  { id: 'family-tutor', file: 'examples/family-learning-hub/template/family-tutor.template.yaml' },
  { id: 'child-desk', file: 'examples/family-learning-hub/template/child-desk.template.yaml' },
]

const seenIds = new Set()
const entries = []
let totalBytes = 0
for (const { id, file } of CURATED) {
  if (seenIds.has(id)) throw new Error(`duplicate builtin template id '${id}'`)
  seenIds.add(id)
  const yaml = await readFile(join(REPO_ROOT, file), 'utf8')
  totalBytes += Buffer.byteLength(yaml)
  // The example dir is the provenance pointer (two levels up from the file).
  const sourceExample = dirname(dirname(file))
  // JSON.stringify, NOT a backtick literal: the manifests contain `${ENV}`
  // secret placeholders that a template literal would try to interpolate.
  entries.push(
    `  { id: ${JSON.stringify(id)}, sourceExample: ${JSON.stringify(sourceExample)}, yaml: ${JSON.stringify(yaml)} },`,
  )
}

const header = `// AUTO-GENERATED by scripts/build-builtin-templates.mjs. Do not edit by hand.
// Run \`pnpm -C packages/web build:templates\` after changing a curated
// examples/*/template/*.template.yaml. Single source of truth lives in
// examples/ (模版与框架分离); this is a derived, embedded copy so the gallery
// works without filesystem access. ${CURATED.length} templates, ${totalBytes} bytes.
`

const body = `\n/** One shipped, installable \`gotong.template/v1\` manifest for the gallery. */
export interface BuiltinTemplate {
  /** Stable gallery id (decoupled from the source filename). */
  id: string
  /** Provenance: the example directory this manifest was bundled from. */
  sourceExample: string
  /** The raw \`gotong.template/v1\` manifest text — install passes it to the importer. */
  yaml: string
}

export const BUILTIN_TEMPLATES: BuiltinTemplate[] = [\n${entries.join('\n')}\n]\n`

await writeFile(OUT_FILE, header + body, 'utf8')

process.stdout.write(
  `build-builtin-templates: ${CURATED.length} templates, ${totalBytes} bytes embedded → ${OUT_FILE}\n`,
)
